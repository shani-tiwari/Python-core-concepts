# Module - single file containing code, we can use this file code to another file using import keyword 
# syntax - import module_name 

# example - 
import math         # math.py file should be in same directory or in python path 
print(math.sqrt(16)) 


# Package - collection of modules in a directory (folder) 
# syntax - import package_name 


# import statements examples
from math import sqrt   #  import specific function 
from math import *      # import all functions 
import math             # import whole module 
